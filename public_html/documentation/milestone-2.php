		<h1>Content Strategy</h1>
		<p>My site will be one page, and will consist of the following parts: </p>
		<p><strong>Welcome: </strong>This section will be the first thing the user will see as it will be overlaid on a solid color background and display my name and state I am a web developer. Underneath my name will be three links that will navigate the user to the other parts of my website: <em>About Me, Portfolio, Contact Me.</em></p>
		<p><strong>About Me: </strong>My about me section will let the user read about who I am. It will have a condensed version of how I started web developing and the languages I have learned during the bootcamp. I will also include my story of where I am from and some of the interests I have as I want to build some sort of connection with my users. The tone of the whole website will be business-casual as I want to establish that my website is mainly for business, but still have a connection with the client or organizations who like what I have to offer. </p>
		<p><strong>Portfolio: </strong>This section will consist of screen shots of my projects that I did in the bootcamp and one sentence simply saying "Here is a link to my projects I have been working on," with a link to my github profile.</p>
		<p><strong>Contact: </strong>This section of the website will have all my contact information which will include my professional media profiles such as: Github, LinkedIn and my email address.</p>
